-- ============================================================
-- 잠재고객 예상자산 단위 환산: 백만원 → 억원 (1회만 실행!)
-- 예: 30,000(백만원) → 300(억원)
-- ⚠️ 두 번 실행하면 값이 또 100으로 나눠지니 반드시 1회만!
-- ============================================================

update public.prospects
set expected_asset = round(expected_asset / 100.0, 2)
where expected_asset is not null;
